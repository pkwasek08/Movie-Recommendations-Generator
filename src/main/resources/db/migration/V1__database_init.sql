(
    "id"        SERIAL PRIMARY KEY,
    "login"     varchar(16) COLLATE pg_catalog."default" NOT NULL,
    "firstname" varchar(30) COLLATE pg_catalog."default" NOT NULL,
    "lastname"  varchar(40) COLLATE pg_catalog."default" NOT NULL,
    "email"     varchar(60) COLLATE pg_catalog."default" NOT NULL,
